const defaults = { scalar: 1.4, colors: ["#e20074", "#a61765"] };

const generateConfetti = ({ corners, ticks, zIndex, particleCount, emojis = [], svgs = [] }) => {
  const dynamicVelocity = Math.max(45, window.innerWidth / 15);
  const config = { ...defaults };

  // 1. Prepare Emojis
  const emojiShapes = emojis?.map((text) => confetti.shapeFromText({ text, scalar: 3 })) || [];

  // 2. Map SVGs
  const svgShapes = svgs.map((cfg) => ({
    shape: confetti.shapeFromPath({ path: cfg.path }),
    color: cfg.color,
    scalar: cfg.scalar || 5,
  }));

  const fire = (xOrigin, yOrigin, angle) => {
    const base = { angle, spread: 100, ticks: ticks, zIndex: zIndex, origin: { x: xOrigin, y: yOrigin } };

    // Standard confetti burst (with default colors)
    confetti({
      ...config,
      ...base,
      particleCount: particleCount / corners,
      startVelocity: dynamicVelocity,
      gravity: 0.7,
    });
    confetti({
      ...config,
      ...base,
      particleCount: particleCount / 10,
      startVelocity: dynamicVelocity * 0.35,
      gravity: 1,
      ticks: ticks / 2,
    });

    // Emoji burst
    if (emojiShapes.length > 0) {
      confetti({
        ...base,
        shapes: emojiShapes,
        particleCount: 15,
        scalar: 5,
        startVelocity: dynamicVelocity,
      });
    }

    // SVG burst (with individual color locking)
    svgShapes.forEach(({ shape, color, scalar }) => {
      confetti({
        ...base,
        shapes: [shape], // Must be an array
        colors: [color], // Locks the SVG to this specific color
        particleCount: 12,
        scalar: scalar,
        startVelocity: dynamicVelocity,
        gravity: 0.7,
      });
    });
  };

  // Launch from corners
  fire(0, 1, 60);
  fire(1, 1, 120);

  if (corners === 4) {
    fire(0, 0, 320);
    fire(1, 0, 210);
  }
};

