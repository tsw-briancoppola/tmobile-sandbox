const runConfetti = (() => {
  let last = 0;
  const delay = 1000; // 1 second delay

  return (fn, ...args) => {
    const now = Date.now();
    if (now - last >= delay) {
      last = now;
      fn(...args); // Correctly applies the arguments here
    }
  };
})();

